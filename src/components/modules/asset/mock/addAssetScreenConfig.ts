// Helper to get sections by category
export const getSectionsByCategory = (_category: string, formData: Record<string, any>) => {
  return addAssetScreenConfig.sections.filter(section => {
    // If section has no conditional display, it's always shown
    if (!section.conditionalDisplay || section.conditionalDisplay.length === 0) {
      return true;
    }
    
    // Check if all conditions are met
    return section.conditionalDisplay.every(rule => {
      const fieldValue = formData[rule.fieldId] || formData[getFieldNameFromId(rule.fieldId)];
      switch (rule.operator) {
        case 'equals':
          return fieldValue === rule.value;
        case 'in':
          return Array.isArray(rule.value) && rule.value.includes(fieldValue);
        default:
          return true;
      }
    });
  });
};

// Helper to map field IDs to field names
